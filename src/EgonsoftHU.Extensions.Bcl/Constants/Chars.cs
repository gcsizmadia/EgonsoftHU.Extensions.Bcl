using System;

namespace EgonsoftHU.Extensions.Bcl.Constants
{
    /// <summary>
    /// A collection of frequently used <see cref="Char"/> constants.
    /// </summary>
    public static class Chars
    {
        /// <summary>
        /// The colon (<c>:</c>) character.
        /// </summary>
        public const char Colon = ':';

        /// <summary>
        /// The comma (<c>,</c>) character.
        /// </summary>
        public const char Comma = ',';

        /// <summary>
        /// The dot (<c>.</c>) character.
        /// </summary>
        public const char Dot = '.';

        /// <summary>
        /// The double quotation mark (<c>"</c>) character.
        /// </summary>
        public const char DoubleQuotationMark = '\"';

        /// <summary>
        /// The hyphen (<c>-</c>) character.
        /// </summary>
        public const char Hyphen = '-';

        /// <summary>
        /// The semicolon (<c>;</c>) character.
        /// </summary>
        public const char Semicolon = ';';

        /// <summary>
        /// The slash (<c>/</c>) character.
        /// </summary>
        public const char Slash = '/';

        /// <summary>
        /// The space character.
        /// </summary>
        public const char Space = ' ';
    }
}
